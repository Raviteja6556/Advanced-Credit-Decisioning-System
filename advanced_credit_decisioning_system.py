"""## Bivariate Analysis for Categorical Features (Training Set)

### Subtask:
For each categorical feature in df_train, perform bivariate analysis against the 'is_bad_loan' target variable. This involves creating stacked bar charts to show the proportion of good/bad loans within each category. Additionally, conduct Chi-square tests of independence and calculate Cramér’s V to quantify the strength of association between categorical features and the target.

### Bivariate Analysis for Categorical Features against 'is_bad_loan'

For each categorical feature in `df_train`, the following bivariate analysis will be performed:
1.  **Stacked Bar Chart**: To visualize the proportion of 'good loans' (False) and 'bad loans' (True) within each category of the feature.
2.  **Chi-square Test of Independence**: To statistically assess if there is a significant association between the categorical feature and 'is_bad_loan'.
3.  **Cramér's V**: To quantify the strength of the association between the two categorical variables, providing a measure of effect size.

**Reasoning**:
Now, I will iterate through each categorical column in `df_train` to perform bivariate analysis by generating stacked bar charts, conducting Chi-square tests of independence, and calculating Cramér’s V, as specified in the subtask instructions.
"""

from scipy.stats import chi2_contingency
import numpy as np

def cramers_v(confusion_matrix):
    """ Calculate Cramér's V statistic for a contingency table. """
    chi2 = chi2_contingency(confusion_matrix)[0]
    n = confusion_matrix.sum().sum()
    r, k = confusion_matrix.shape
    # Handle cases where k or r might be 1 to avoid division by zero
    phi2 = chi2 / n
    if min(k - 1, r - 1) == 0:
        return 0.0 # Return 0 if one of the dimensions is 1 (no association possible)
    v = np.sqrt(phi2 / min(k - 1, r - 1))
    return v

for col in categorical_cols_train:
    if col == 'loan_status': # Skip 'loan_status' as it's the direct source for 'is_bad_loan'
        continue

    print(f"\n--- Bivariate Analysis for {col} vs. is_bad_loan ---")

    # 1. Contingency Table
    contingency_table = pd.crosstab(df_train[col], df_train['is_bad_loan'])
    print("Contingency Table:")
    display(contingency_table)

    # 2. Stacked Bar Chart
    plt.figure(figsize=(12, 7))
    # Normalize by index to get proportions within each category
    contingency_table_normalized = contingency_table.div(contingency_table.sum(axis=1), axis=0)
    contingency_table_normalized.plot(kind='bar', stacked=True, figsize=(12, 7), colormap='viridis')
    plt.title(f'Proportion of Bad Loans by {col}')
    plt.xlabel(col)
    plt.ylabel('Proportion')
    plt.xticks(rotation=45, ha='right')
    plt.legend(title='Is Bad Loan', labels=['Good Loan', 'Bad Loan'])
    plt.tight_layout()
    plt.show()

    # 3. Chi-square Test of Independence
    if not contingency_table.empty and (contingency_table.sum(axis=1) > 0).all():
        chi2, p_value, dof, expected = chi2_contingency(contingency_table)
        print(f"Chi-square Test for {col}:")
        print(f"  Chi-square statistic: {chi2:.4f}")
        print(f"  P-value: {p_value:.4f}")

        if p_value < 0.05:
            print("  Interpretation: There is a statistically significant association between this feature and loan status (p < 0.05).")
        else:
            print("  Interpretation: No statistically significant association between this feature and loan status (p >= 0.05).")
    else:
        print(f"  Not enough data or invalid contingency table for Chi-square test for {col}.")

    # 4. Cramér's V
    if not contingency_table.empty and (contingency_table.sum().sum() > 0):
        v_score = cramers_v(contingency_table)
        print(f"Cramér's V for {col}: {v_score:.4f}")
        if v_score < 0.1:
            print("  Interpretation: Very weak association.")
        elif v_score < 0.2:
            print("  Interpretation: Weak association.")
        elif v_score < 0.4:
            print("  Interpretation: Moderate association.")
        elif v_score < 0.6:
            print("  Interpretation: Strong association.")
        else:
            print("  Interpretation: Very strong association.")
    else:
        print(f"  Not enough data or invalid contingency table for Cramér's V for {col}.")

