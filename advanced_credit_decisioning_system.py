# Re-define the inference_system function (copy-pasted for standalone nature)
def inference_system(applicant_data: dict, preprocessor, calibrated_model, shap_explainer, best_threshold, top_n_features=5):
    """
    Predicts loan decision and probability of default for a new applicant,
    and provides local SHAP explanations.

    Args:
        applicant_data (dict): A dictionary of new applicant parameters.
        preprocessor: The fitted ColumnTransformer preprocessing pipeline.
        calibrated_model: The fitted CalibratedClassifierCV model.
        shap_explainer: The fitted SHAP TreeExplainer.
        best_threshold (float): The optimal decision threshold.
        top_n_features (int): Number of top SHAP features to display.

    Returns:
        dict: A dictionary containing prediction, probability of default, and SHAP explanations.
    """

    # 1. Create a DataFrame from applicant_data, ensuring correct column order
    input_df = pd.DataFrame([applicant_data], columns=X_train_columns) # Use loaded columns

    # 2. Apply the trained preprocessing pipeline
    processed_input = preprocessor.transform(input_df)

    # Convert processed_input sparse matrix to dense DataFrame for SHAP explanation
    # The feature names derived from the preprocessor are needed here
    ohe_feature_names = preprocessor.named_transformers_['cat'].named_steps['onehot'].get_feature_names_out(categorical_features)
    processed_feature_names_inference = numerical_features + ohe_feature_names.tolist()

    processed_input_df = pd.DataFrame(
        processed_input.toarray(),
        columns=processed_feature_names_inference
    )

    # 3. Use the calibrated champion model to predict PD and decision
    prob_default = calibrated_model.predict_proba(processed_input)[:, 1][0]
    decision = 'Reject' if prob_default >= best_threshold else 'Approve'

    # 4. Provide top reasons using local SHAP explanations
    shap_values_output = shap_explainer.shap_values(processed_input_df.iloc[[0]])

    if isinstance(shap_values_output, list):
        # For TreeExplainer, shap_values returns a list of arrays for multi-class, or a single array for binary
        if len(shap_values_output) == 2:
            shap_values_instance = shap_values_output[1][0] # Class 1 SHAP values, first instance
        else:
            shap_values_instance = shap_values_output[0]
    else: # If shap_values_output is directly the 2D array for the positive class
        shap_values_instance = shap_values_output[0]

    expected_value_for_plot = shap_explainer.expected_value


    # Create a DataFrame for SHAP values and sort by absolute magnitude
    shap_df = pd.DataFrame({
        'feature': processed_feature_names_inference,
        'shap_value': shap_values_instance
    }).sort_values(by='shap_value', ascending=False, key=abs)

    # Get top N features contributing to the prediction
    top_shap_features = shap_df.head(top_n_features)
    explanation = top_shap_features.to_dict(orient='records')

    # Generate a local SHAP force plot
    print(f"\nSHAP Force Plot for this {decision} decision:")
    shap.initjs()
    shap.force_plot(expected_value_for_plot, shap_values_instance, processed_input_df.iloc[0], matplotlib=True, show=False)
    plt.title(f'Local Explanation for {decision} (PD: {prob_default:.4f})')
    plt.show()

    return {
        'loan_decision': decision,
        'probability_of_default': prob_default,
        'top_shap_reasons': explanation
    }

